#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	int		src_l;

	if ((!dst || !src) && !dstsize)
		return (0);
	src_l = ft_strlen(src);
	if (!dstsize)
		return (src_l);
	i = 0;
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_l);
}
